const useToolbarStyles = makeStyles((theme) => ({
  root: {
    paddingLeft: theme.spacing(2),
    paddingRight: theme.spacing(4),
  },
  highlight:
    theme.palette.type === 'light'
      ? {
          color: theme.palette.secondary.main,
          backgroundColor: lighten(theme.palette.secondary.light, 0.85),
        }
      : {
          color: theme.palette.text.primary,
          backgroundColor: theme.palette.secondary.dark,
        },
  title: {
    flex: '1 1 100%',
  },
}));

const EnhancedTableToolbar = (props) => {
  const classes = useToolbarStyles();
  const { numSelected, selected } = props;
  const [open, setOpen] = React.useState(false);
	const [message, setMessage] = React.useState("");

  const handleAlertClose = (event, reason) => {
    if (reason === 'clickaway') {
      setOpen(false);
    }
    setOpen(false);
  };

  const handleDelete = () => {
    // console.log(selected);
    for(let i = 0; i < numSelected; i++) {
      // console.log(selected[i]);
      axiosInstance.delete("pantry/ingredient/delete/" + selected[i] + "/")
        .then((response) => {
          props.resetSelected();
          props.refetchData();
        })
        .catch((error) => console.log(error));
    }
    setOpen(true);
    setMessage(numSelected + " ingredient(s) deleted!");
  }

  return (
    <Toolbar
      className={clsx(classes.root, {
        [classes.highlight]: numSelected > 0,
      })}
    >
      <Alert open={open} message={message} handleClose={handleAlertClose}/>
      {numSelected > 0 ? (
        <Typography className={classes.title} color="inherit" variant="subtitle1" component="div">
          {numSelected} selected
        </Typography>
      ) : (
        <Typography className={classes.title} variant="h5" id="tableTitle" component="div" color="primary">
          Pantry
        </Typography>
      )}

      {numSelected > 0 ? (
        <Tooltip title="Delete" onClick={handleDelete}>
          <IconButton color='secondary' aria-label="delete">
            <DeleteIcon />
          </IconButton>
        </Tooltip>
      ) : (
        <Tooltip title="Add Ingredient">
          {/* <IconButton aria-label="add ingredient"> */}
            <PantryForm 
              title={<AddIcon />}
              user={props.user}      
              refetchData={props.refetchData}
            >
            </PantryForm>
          {/* </IconButton> */}
        </Tooltip>
      )}
    </Toolbar>
  );
};

EnhancedTableToolbar.propTypes = {
  numSelected: PropTypes.number.isRequired,
};

const useStyles = makeStyles((theme) => ({
  root: {
    width: '100%',
  },
  paper: {
    width: '100%',
    marginBottom: theme.spacing(2),
  },
  table: {
    minWidth: 750,
  },
  visuallyHidden: {
    border: 0,
    clip: 'rect(0 0 0 0)',
    height: 1,
    margin: -1,
    overflow: 'hidden',
    padding: 0,
    position: 'absolute',
    top: 20,
    width: 1,
  },
  appBarSeparator: {
    marginTop: theme.spacing(10),
    margin: theme.spacing(1),
  },
}));

export default function EnhancedTable() {
  const classes = useStyles();
  const [order, setOrder] = React.useState('asc');
  const [orderBy, setOrderBy] = React.useState('name');
  const [selected, setSelected] = React.useState([]);
  const [page, setPage] = React.useState(0);
  const [dense, setDense] = React.useState(true);
  const [rowsPerPage, setRowsPerPage] = React.useState(10);
  const [data, setData] = React.useState([{}]);
  const [refetchSwitch, triggerSwitch] = React.useState(false);
  const [loaded, setLoaded] = React.useState(false);




  useEffect(() => {
    axiosInstance.get("pantry/all")
      .then((response) => {
      // console.log(response.data);
      // console.log("Made a request for ingredients");
      setData(response.data);
      setLoaded(true);
    })
      .catch((error) => console.log(error));
  }, [refetchSwitch]);

  const refetchData = () => {
    // console.log("Refetching data");
    triggerSwitch(!refetchSwitch);
  };

  const resetSelected = () => {
    setSelected([]);
  }

  const handleRequestSort = (event, property) => {
    const isAsc = orderBy === property && order === 'asc';
    setOrder(isAsc ? 'desc' : 'asc');
    setOrderBy(property);
  };

  const handleSelectAllClick = (event) => {
    if (event.target.checked) {
      const newSelecteds = data.map((n) => n.id);
      setSelected(newSelecteds);
      return;
    }
    setSelected([]);
  };

  const handleClick = (event, name) => {
    const selectedIndex = selected.indexOf(name);
    let newSelected = [];

    if (selectedIndex === -1) {
      newSelected = newSelected.concat(selected, name);
    } else if (selectedIndex === 0) {
      newSelected = newSelected.concat(selected.slice(1));
    } else if (selectedIndex === selected.length - 1) {
      newSelected = newSelected.concat(selected.slice(0, -1));
    } else if (selectedIndex > 0) {
      newSelected = newSelected.concat(
        selected.slice(0, selectedIndex),
        selected.slice(selectedIndex + 1),
      );
    }

    setSelected(newSelected);
  };

  const handleChangePage = (event, newPage) => {
    setPage(newPage);
  };

  const handleChangeRowsPerPage = (event) => {
    setRowsPerPage(parseInt(event.target.value, 10));
    setPage(0);
  };

  const handleChangeDense = (event) => {
    setDense(event.target.checked);
  };

  const isSelected = (name) => selected.indexOf(name) !== -1;

  const emptyRows = rowsPerPage - Math.min(rowsPerPage, data.length - page * rowsPerPage);

  return (
    // <div className={classes.root, classes.appBarSeparator}>
    <Fade in={loaded}>
      <Grid item xs={12} className={classes.root, classes.appBarSeparator}>
      <Paper className={classes.paper}>
        <EnhancedTableToolbar 
          numSelected={selected.length} 
          selected={selected} 
          refetchData={refetchData} 
          resetSelected={resetSelected}
          user={localStorage.getItem('user_id')}
          />
        <TableContainer>
          <Table
            className={classes.table}
            aria-labelledby="tableTitle"
            size={dense ? 'small' : 'medium'}
            aria-label="enhanced table"
          >
            <EnhancedTableHead
              classes={classes}
              numSelected={selected.length}
              order={order}
              orderBy={orderBy}
              onSelectAllClick={handleSelectAllClick}
              onRequestSort={handleRequestSort}
              rowCount={data.length}
            />
            <TableBody>
              {stableSort(data, getComparator(order, orderBy))
                .slice(page * rowsPerPage, page * rowsPerPage + rowsPerPage)
                .map((row, index) => {
                  const isItemSelected = isSelected(row.id);
                  const labelId = `enhanced-table-checkbox-${index}`;

                  return (
                    <TableRow
                      hover
                      // onClick={(event) => handleClick(event, row.id)}
                      role="checkbox"
                      aria-checked={isItemSelected}
                      tabIndex={-1}
                      key={row.id}
                      selected={isItemSelected}
                    >
                      <TableCell padding="checkbox">
                        <Checkbox
                          checked={isItemSelected}
                          inputProps={{ 'aria-labelledby': labelId }}
                          onClick={(event) => handleClick(event, row.id)}
                        />
                      </TableCell>
                      <TableCell component="th" id={labelId} scope="row" padding="none">
                        {row.name}
                      </TableCell>
                      <TableCell>{row.amount}</TableCell>
                      <TableCell>{row.unit}</TableCell>
                      <TableCell>{row.recipe_count}</TableCell>
                      <TableCell>{row.created && formatRelative(parseISO(row.created), new Date())}</TableCell>
                      <TableCell>{row.modified && formatDistance(parseISO(row.modified), new Date(), {addSuffix: true})}</TableCell>
                      <TableCell align="center">
                        <PantryForm 
                          title={<EditIcon/>}
                          user={row.user}
                          item-id={row.id}
                          item-name={row.name}
                          item-unit={row.unit}
                          item-amount={row.amount}
                          refetchData={refetchData}
                        >
                        </PantryForm>
                      </TableCell>
                    </TableRow>
                  );
                })}
              {emptyRows > 0 && (
                <TableRow style={{ height: (dense ? 33 : 53) * emptyRows }}>
                  <TableCell colSpan={6} />
                </TableRow>
              )}
            </TableBody>
          </Table>
        </TableContainer>
        <TablePagination
          rowsPerPageOptions={[5, 10, 25]}
          component="div"
          count={data.length}
          rowsPerPage={rowsPerPage}
          page={page}
          onPageChange={handleChangePage}
          onRowsPerPageChange={handleChangeRowsPerPage}
        />
      </Paper>
      <FormControlLabel
        control={<Switch checked={dense} color="primary" onChange={handleChangeDense} />}
        label="Dense padding"
      />
      </Grid>
      </Fade>
  );
}